var edad = 18;

switch (edad) {
  case 18:
    console.log("Acaba de cumplir la mayoria de edad");
    break;
  case 13:
    console.log("Acaba de ingresar a la secundaria");
    break;
  case 30:
    console.log("Estas bastante maduro ya");
    break;
  case 70:
    console.log("Ya deberias jubilarte");
    break;
  default:
    console.log("Edad no catalogada");
    break;
}
